import { Cell } from "./Cell.js";

export function stageMaker(game, stage) {
  let cells = [];

  stage.forEach((row, rowIndex) => {
    row.forEach((cellColor, cellIndex) => {
      let position = {
        x: 32 + 32 * cellIndex,
        y: 64 + 32 * rowIndex,
      };

      cells.push(new Cell(game, position, cellColor));
    });
  });

  return cells;
}

